# -*- coding: utf-8 -*-
"""Run OpenCOOD inference and evaluate with OpenPCDet Sunlakes metrics."""

import argparse
import json
import sys
from collections import OrderedDict
from pathlib import Path

import torch
from torch.utils.data import DataLoader, Dataset
from tqdm import tqdm

ROOT_DIR = Path(__file__).resolve().parents[2]
if str(ROOT_DIR) not in sys.path:
    sys.path.insert(0, str(ROOT_DIR))


def parse_args():
    parser = argparse.ArgumentParser(description='OpenCOOD Sunlakes evaluation bridge')
    parser.add_argument('--model_dir', type=str, required=True, help='OpenCOOD checkpoint directory')
    parser.add_argument('--checkpoint', type=str, default='',
                        help='Optional checkpoint filename or absolute path to evaluate')
    parser.add_argument('--fusion_method', type=str, required=True, choices=['late', 'early', 'intermediate'])
    parser.add_argument('--openpcdet_data_root', type=str, required=True,
                        help='Root containing sunlakes_infos_val.pkl generated by OpenPCDet converter')
    parser.add_argument('--openpcdet_root', type=str, default='/home/suolab/OpenPCDet',
                        help='OpenPCDet repository root for importing sunlakes_utils')
    parser.add_argument('--class_name', type=str, default='car',
                        help='Fallback class name when model does not expose labels')
    parser.add_argument('--val_info_name', type=str, default='sunlakes_infos_val.pkl',
                        help='Validation info filename used by sunlakes_utils.evaluate')
    parser.add_argument('--save_dir', type=str, default='',
                        help='Optional output directory; default: <model_dir>/sunlakes_eval')
    parser.add_argument('--num_workers', type=int, default=0,
                        help='Dataloader worker count for evaluation')
    parser.add_argument('--max_samples', type=int, default=0,
                        help='Optional cap on evaluated samples for smoke tests; 0 means full set')
    parser.add_argument('--shard_id', type=int, default=0,
                        help='Shard id for multi-GPU evaluation')
    parser.add_argument('--num_shards', type=int, default=1,
                        help='Total number of shards for multi-GPU evaluation')
    parser.add_argument('--aggregate_only', action='store_true',
                        help='Only merge shard outputs and run metric evaluation')
    return parser.parse_args()


def get_sample_token(dataset, idx):
    scenario_index = 0
    for i, ele in enumerate(dataset.len_record):
        if idx < ele:
            scenario_index = i
            break
    scenario_database = dataset.scenario_database[scenario_index]
    timestamp_index = idx if scenario_index == 0 else idx - dataset.len_record[scenario_index - 1]
    return dataset.return_timestamp_key(scenario_database, timestamp_index)


def build_attribute(class_name):
    if class_name == 'pedestrian':
        return 'pedestrian.standing'
    if class_name == 'bus':
        return 'vehicle.stopped'
    if class_name in ['car', 'construction_vehicle', 'truck', 'trailer']:
        return 'vehicle.parked'
    if class_name in ['bicycle', 'motorcycle']:
        return 'cycle.without_rider'
    return ''


def corners_to_result_annos(pred_box_tensor, pred_score, pred_label, sample_token,
                            class_names, default_class_name, box_utils, torch_tensor_to_numpy):
    if pred_box_tensor is None or pred_score is None:
        return []

    pred_np = torch_tensor_to_numpy(pred_box_tensor)
    score_np = torch_tensor_to_numpy(pred_score)
    label_np = None if pred_label is None else torch_tensor_to_numpy(pred_label)
    if pred_np.shape[0] == 0:
        return []

    boxes7 = box_utils.corner_to_center(pred_np, order='hwl')
    annos = []
    for idx, (box, score) in enumerate(zip(boxes7, score_np)):
        yaw = float(box[6])
        if label_np is None:
            class_name = default_class_name
        else:
            class_idx = int(label_np[idx])
            class_name = class_names[class_idx] if 0 <= class_idx < len(class_names) else default_class_name
        annos.append({
            'sample_token': sample_token,
            'translation': [float(box[0]), float(box[1]), float(box[2])],
            'size': [float(box[5]), float(box[4]), float(box[3])],
            'rotation': [float(torch.cos(torch.tensor(yaw / 2)).item()), 0.0, 0.0,
                         float(torch.sin(torch.tensor(yaw / 2)).item())],
            'velocity': [0.0, 0.0],
            'detection_name': class_name,
            'detection_score': float(score),
            'attribute_name': build_attribute(class_name),
            'token': sample_token,
        })
    return annos


class IndexedDataset(Dataset):
    def __init__(self, dataset, indices):
        self.dataset = dataset
        self.indices = indices

    def __len__(self):
        return len(self.indices)

    def __getitem__(self, idx):
        dataset_idx = self.indices[idx]
        return dataset_idx, self.dataset[dataset_idx]


def run_inference(opt, hypes):
    from opencood.data_utils.datasets import build_dataset
    from opencood.tools import inference_utils, train_utils
    from opencood.utils import box_utils
    from opencood.utils.common_utils import torch_tensor_to_numpy

    dataset = build_dataset(hypes, visualize=False, train=False)
    if opt.num_shards < 1:
        raise ValueError('num_shards must be >= 1')
    if opt.shard_id < 0 or opt.shard_id >= opt.num_shards:
        raise ValueError(f'shard_id must be in [0, {opt.num_shards})')

    shard_indices = list(range(opt.shard_id, len(dataset), opt.num_shards))
    if opt.max_samples > 0:
        shard_indices = shard_indices[:opt.max_samples]

    indexed_dataset = IndexedDataset(dataset, shard_indices)

    def collate_indexed_batch(batch):
        assert len(batch) == 1, 'Batch size 1 is required during testing!'
        dataset_idx, sample = batch[0]
        return dataset_idx, dataset.collate_batch_test([sample])

    loader = DataLoader(indexed_dataset, batch_size=1, num_workers=opt.num_workers,
                        collate_fn=collate_indexed_batch,
                        shuffle=False, pin_memory=False, drop_last=False)

    model = train_utils.create_model(hypes)
    if torch.cuda.is_available():
        model.cuda()
    device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    if opt.checkpoint:
        checkpoint_path = Path(opt.checkpoint)
        if not checkpoint_path.is_absolute():
            checkpoint_path = Path(opt.model_dir) / checkpoint_path
        checkpoint = torch.load(str(checkpoint_path), map_location='cpu')
        model.load_state_dict(checkpoint, strict=False)
        del checkpoint
        print(f'loaded checkpoint {checkpoint_path}')
    else:
        _, model = train_utils.load_saved_model(opt.model_dir, model)
    model.eval()

    class_names = getattr(dataset.post_processor, 'anchor_class_names', None)
    if not class_names:
        class_names = hypes.get('postprocess', {}).get('anchor_args', {}).get('class_names', [opt.class_name])

    results = {}
    total = len(loader)
    desc = f'Running inference shard {opt.shard_id + 1}/{opt.num_shards}'
    for dataset_idx, batch_data in tqdm(loader, total=total, desc=desc):
        sample_token = get_sample_token(dataset, int(dataset_idx))
        with torch.no_grad():
            batch_data = train_utils.to_device(batch_data, device)
            if hasattr(dataset.post_processor, 'post_process_with_label'):
                if opt.fusion_method == 'late':
                    output_dict = OrderedDict((cav_id, model(cav_content)) for cav_id, cav_content in batch_data.items())
                else:
                    output_dict = OrderedDict({'ego': model(batch_data['ego'])})
                pred_box_tensor, pred_score, pred_label = dataset.post_processor.post_process_with_label(batch_data, output_dict)
            else:
                if opt.fusion_method == 'late':
                    pred_box_tensor, pred_score, _ = inference_utils.inference_late_fusion(batch_data, model, dataset)
                elif opt.fusion_method == 'early':
                    pred_box_tensor, pred_score, _ = inference_utils.inference_early_fusion(batch_data, model, dataset)
                else:
                    pred_box_tensor, pred_score, _ = inference_utils.inference_intermediate_fusion(batch_data, model, dataset)
                pred_label = None
        results[sample_token] = corners_to_result_annos(
            pred_box_tensor, pred_score, pred_label, sample_token,
            class_names, opt.class_name, box_utils, torch_tensor_to_numpy,
        )

    return results, class_names


def build_result_payload(results):
    return {
        'results': results,
        'meta': {
            'use_camera': False,
            'use_lidar': True,
            'use_radar': False,
            'use_map': False,
            'use_external': False,
        }
    }


def get_shard_result_path(save_dir, shard_id, num_shards):
    return save_dir / f'results_nusc_shard{shard_id}_of_{num_shards}.json'


def merge_shard_results(save_dir, num_shards):
    merged_results = {}
    meta = None
    for shard_id in range(num_shards):
        shard_path = get_shard_result_path(save_dir, shard_id, num_shards)
        with open(shard_path, 'r', encoding='utf-8') as f:
            shard_payload = json.load(f)
        if meta is None:
            meta = shard_payload['meta']
        merged_results.update(shard_payload['results'])
    return {'results': merged_results, 'meta': meta}


def main():
    import opencood.hypes_yaml.yaml_utils as yaml_utils

    opt = parse_args()
    openpcdet_root = Path(opt.openpcdet_root)
    if str(openpcdet_root) not in sys.path:
        sys.path.insert(0, str(openpcdet_root))

    from pcdet.datasets.carla import sunlakes_utils
    from nuscenes.eval.detection.config import config_factory

    save_dir = Path(opt.save_dir) if opt.save_dir else Path(opt.model_dir) / 'sunlakes_eval'
    save_dir.mkdir(parents=True, exist_ok=True)

    hypes = yaml_utils.load_yaml(None, opt)
    class_names = hypes.get('postprocess', {}).get('anchor_args', {}).get('class_names', [opt.class_name])

    if opt.aggregate_only:
        payload = merge_shard_results(save_dir, opt.num_shards)
        result_path = save_dir / 'results_nusc.json'
        with open(result_path, 'w', encoding='utf-8') as f:
            json.dump(payload, f)
    else:
        results, class_names = run_inference(opt, hypes)
        payload = build_result_payload(results)
        if opt.num_shards > 1:
            result_path = get_shard_result_path(save_dir, opt.shard_id, opt.num_shards)
        else:
            result_path = save_dir / 'results_nusc.json'
        with open(result_path, 'w', encoding='utf-8') as f:
            json.dump(payload, f)
        if opt.num_shards > 1:
            print(f'Finished shard {opt.shard_id + 1}/{opt.num_shards}, saved to {result_path}')
            return

    try:
        eval_config = config_factory('detection_cvpr_2019')
    except Exception:
        eval_config = config_factory('cvpr_2019')
    eval_config.class_names = class_names

    metrics_summary = sunlakes_utils.evaluate(
        root_path=opt.openpcdet_data_root,
        output_dir=save_dir,
        result_path=str(result_path),
        cfg=eval_config,
        val_name=[opt.val_info_name],
    )
    print(f"Finished Sunlakes evaluation. mean_ap={metrics_summary['mean_ap']:.4f}")


if __name__ == '__main__':
    main()
